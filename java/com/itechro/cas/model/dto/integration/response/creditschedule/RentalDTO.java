package com.itechro.cas.model.dto.integration.response.creditschedule;

public class RentalDTO {
    private String capAmt;
    private String intAmt;
    private String capitalOs;
    private String grssRentOs;

    public String getCapAmt() {
        return capAmt;
    }

    public void setCapAmt(String capAmt) {
        this.capAmt = capAmt;
    }

    public String getIntAmt() {
        return intAmt;
    }

    public void setIntAmt(String intAmt) {
        this.intAmt = intAmt;
    }

    public String getCapitalOs() {
        return capitalOs;
    }

    public void setCapitalOs(String capitalOs) {
        this.capitalOs = capitalOs;
    }

    public String getGrssRentOs() {
        return grssRentOs;
    }

    public void setGrssRentOs(String grssRentOs) {
        this.grssRentOs = grssRentOs;
    }
}
